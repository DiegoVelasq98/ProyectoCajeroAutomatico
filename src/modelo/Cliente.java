package modelo;

/**
 *
 * @author Diego
 */
public class Cliente {
    //pruebas para validar el push
    private int id;
    private String nombre;
    private double saldo;
    private int pin;
    
    public Cliente (){}

    public Cliente(int id, String nombre, double saldo, int pin) {
        this.id = id;
        this.nombre = nombre;
        this.saldo = saldo;
        this.pin = pin;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

    public int getPin() {
        return pin;
    }

    public void setPin(int pin) {
        this.pin = pin;
    }
    
    

}
